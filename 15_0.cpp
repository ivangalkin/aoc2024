const std::map<char, std::tuple<ll, ll>> DIRECTIONS{
    {'<', {-1, 0}}, {'>', {1, 0}}, {'^', {0, -1}}, {'v', {0, 1}}};

std::tuple<ll, ll> next_pos(std::tuple<ll, ll> r, char d) {
  auto [diff_x, diff_y] = DIRECTIONS.at(d);
  auto [pos_x, pos_y] = r;
  auto &[next_x, next_y] = r;
  next_x += diff_x;
  next_y += diff_y;
  return {next_x, next_y};
}

std::tuple<ll, ll> step(std::tuple<ll, ll> r, char d) {
  auto [next_x, next_y] = next_pos(r, d);
  auto next_c = map.at(next_y).at(next_x);
  if (next_c == '#') {
    return r;
  }
  if (next_c == '.') {
    return {next_x, next_y};
  }

  bool found_empty_slot = false;
  std::tuple<ll, ll> search{next_x, next_y};
  if (next_c == 'O') {
    while (true) {
      auto [next_next_x, next_next_y] = next_pos(search, d);
      auto next_next_c = map.at(next_next_y).at(next_next_x);
      if (next_next_c == 'O') {
        search = {next_next_x, next_next_y};
        continue;
      }
      if (next_next_c == '#') {
        break;
      }
      if (next_next_c == '.') {
        found_empty_slot = true;
        map.at(next_next_y).at(next_next_x) = 'O';
        break;
      }
    }
  }
  if (!found_empty_slot) {
    return r;
  }
  map.at(next_y).at(next_x) = '.';
  return {next_x, next_y};
}
